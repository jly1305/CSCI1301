//This program shows variable assignment
public class initialize
{
   public static void main(String[] args)
   {
      int month, days;
         //you can define variables on the same line as well
         //for example int month = 2, days = 28
         //values must be defined or there will be a syntax error
         month = 2;
         days = 28;
         System.out.println("Month " + month + " has " +
                           days + " Days.");
   }
}
